"""
Tipo Float
"""

# Tupla.
valor = 1, 44
print(valor)
print(type(valor))

# Float.
valor = 1.44
print(valor)
print(type(valor))

# Cast para inteiro.
valor1 = int(valor)
print(valor1)
print(type(valor1))

# Complexo.
valor = 1 + 2j
print(valor)
print(type(valor))

# Cast para float.
valor2 = float(1)
print(valor2)
print(type(valor2))

